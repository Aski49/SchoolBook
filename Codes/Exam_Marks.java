    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/schoolbook","root","");
            pst = con.prepareStatement("SELECT `sname`, `sgrade` FROM `student` WHERE sindex=?");
            
            int id = Integer.parseInt(mindex.getText());
            pst.setInt(1, id);
            ResultSet rs1 = pst.executeQuery();

            if(rs1.next() == false)
            {
                JOptionPane.showMessageDialog(this, "Sorry account Index not available");
                mindex.setText("");
                mindex.requestFocus();
            }

            else
            {
                mname.setText(rs1.getString("sname"));
                mgrade.setText(rs1.getString("sgrade"));
                
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Exam_Marks.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Exam_Marks.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

