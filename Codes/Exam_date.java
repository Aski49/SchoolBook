    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();

        int tid = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

        String tdate = date.getText();
        String ttime = time.getText();
        String tsubject = subject.getSelectedItem().toString();
        String tgrade = grade.getSelectedItem().toString();

        String sql = "update timetable set ttdate=?, tttime=?, ttsubject=?, ttgrade=? where ttid=?";
        // String sql = "UPDATE `student` SET `sid`='',`index`='',`sname`='',`sgender`='',`sdob`='',`saddress`='',`smobile`='',`sgrade`='' WHERE sid=?";

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/schoolbook","root","");
            // pst = con.prepareStatement("Update student set index=?, sname=?, sgender=?, sdob=?, saddress=?, smobile=?, sgrade=? where sid=?");
            pst = con.prepareStatement(sql);

            pst.setString(1,tdate);
            pst.setString(2,ttime);
            pst.setString(3,tsubject);
            pst.setString(4,tgrade);
            pst.setInt(5,tid );

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Timetable successfully Updated.");

            table_update();

            date.setText("");
            time.setText("");
            subject.setSelectedIndex(-1);
            grade.setSelectedIndex(-1);

            date.requestFocus();

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Exam_date.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Exam_date.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

